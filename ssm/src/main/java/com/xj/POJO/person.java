package com.xj.POJO;

public class person {
	private Integer id;
	private String name;
	private String age;
	private idcard idcarder;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAge() {
		return age;
	}
	public void setAge(String age) {
		this.age = age;
	}
	public idcard getIdcarder() {
		return idcarder;
	}
	public void setIdcarder(idcard idcarder) {
		this.idcarder = idcarder;
	}
	
}
